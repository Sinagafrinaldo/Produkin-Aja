export const GET_PRODUK = 'GET_PRODUK';

const API_URL = 'https://api-produk-pam-joy-frinaldo.herokuapp.com/api/produk';

export const getAllProduk = () => {
    try {
        return async dispatch => {
            const result = await fetch(API_URL, {
                method: 'GET',
                headers: {
                    'Content-Type': 'application/json',
                },
            });
            const json = await result.json();
            if (json) {
                dispatch({
                    type: GET_PRODUK,
                    payload: json
                });
            } else {
                console.log('Unable to fetch!');
            }
        }
    } catch (error) {
        console.log(error);
    }
}

